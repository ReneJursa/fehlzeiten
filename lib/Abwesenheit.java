package lib;

public class Abwesenheit {
	
	private int entAbw = 0;
	private int unEntAbw = 0;
	
	public Abwesenheit(int entAbw, int unEntAbw) {
		this.entAbw = entAbw;
		this.unEntAbw = unEntAbw;
	}
	public int getEntAbw() {
		return entAbw;
	}
	public void setEntAbw(int entAbw) {
		this.entAbw = entAbw;
	}
	public int getUnEntAbw() {
		return unEntAbw;
	}
	public void setUnEntAbw(int unEntAbw) {
		this.unEntAbw = unEntAbw;
	}
}	
	
